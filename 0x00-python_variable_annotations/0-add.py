#!/usr/bin/env python3

def add(a: float, b: float) -> float:
    """
    This function takes two float values, a and b, and returns their sum as a float.

    Args:
        a (float): The first float value.
        b (float): The second float value.

    Returns:
        float: The sum of a and b as a float.
    """
    return a + b

